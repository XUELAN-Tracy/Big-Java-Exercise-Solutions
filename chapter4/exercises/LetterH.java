package chapter4.exercises;

/**
 * A program that can produce H as a special graph.
 * @author LanXue
 */
public class LetterH {
    @Override
    /**
     * Overrides the toString method to print H.
     */
    public String toString() {
        return "*   *" +
                "\n*   *" +
                "\n*****" +
                "\n*   *" +
                "\n*   *";
    }
}
