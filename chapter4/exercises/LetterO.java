package chapter4.exercises;

/**
 * A program that can produce O as a special graph.
 * @author LanXue
 */
public class LetterO {

    @Override
    /**
     * Overrides the toString method to print E.
     */
    public String toString() {
        return " ** " +
                "\n*  *" +
                "\n*  *" +
                "\n*  *" +
                "\n ** ";
    }

}
