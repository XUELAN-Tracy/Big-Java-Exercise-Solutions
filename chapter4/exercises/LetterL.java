package chapter4.exercises;

/**
 * A program that can produce L as a special graph.
 * @author LanXue
 */
public class LetterL {

    @Override
    /**
     * Overrides the toString method to print E.
     */
    public String toString() {
        return "*   " +
                "\n*   " +
                "\n*   " +
                "\n*   " +
                "\n****";
    }
}
