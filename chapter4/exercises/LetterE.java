package chapter4.exercises;

/**
 * A program that can produce E as a special graph.
 * @author LanXue
 */
public class LetterE {
    @Override
    /**
     * Overrides the toString method to print E.
     */
    public String toString() {
        return "****" +
                "\n*   " +
                "\n****" +
                "\n*   " +
                "\n****";
    }
}
